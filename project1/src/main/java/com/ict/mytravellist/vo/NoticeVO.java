package com.ict.mytravellist.vo;

public class NoticeVO {
	private String noticeIdx, noticeSubject, noticeFile, noticeContent, noticeReg, noticeLevel, noticeStatus, noticeEtc01, noticeEtc02;

	public String getNoticeIdx() {
		return noticeIdx;
	}

	public void setNoticeIdx(String noticeIdx) {
		this.noticeIdx = noticeIdx;
	}

	public String getNoticeSubject() {
		return noticeSubject;
	}

	public void setNoticeSubject(String noticeSubject) {
		this.noticeSubject = noticeSubject;
	}

	public String getNoticeFile() {
		return noticeFile;
	}

	public void setNoticeFile(String noticeFile) {
		this.noticeFile = noticeFile;
	}

	public String getNoticeContent() {
		return noticeContent;
	}

	public void setNoticeContent(String noticeContent) {
		this.noticeContent = noticeContent;
	}

	public String getNoticeReg() {
		return noticeReg;
	}

	public void setNoticeReg(String noticeReg) {
		this.noticeReg = noticeReg;
	}

	public String getNoticeLevel() {
		return noticeLevel;
	}

	public void setNoticeLevel(String noticeLevel) {
		this.noticeLevel = noticeLevel;
	}

	public String getNoticeStatus() {
		return noticeStatus;
	}

	public void setNoticeStatus(String noticeStatus) {
		this.noticeStatus = noticeStatus;
	}

	public String getNoticeEtc01() {
		return noticeEtc01;
	}

	public void setNoticeEtc01(String noticeEtc01) {
		this.noticeEtc01 = noticeEtc01;
	}

	public String getNoticeEtc02() {
		return noticeEtc02;
	}

	public void setNoticeEtc02(String noticeEtc02) {
		this.noticeEtc02 = noticeEtc02;
	}
}