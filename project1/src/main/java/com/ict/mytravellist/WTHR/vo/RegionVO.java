package com.ict.mytravellist.WTHR.vo;

public class RegionVO {
	private String region, reg_id_short, reg_name, nx, ny, reg_id_long;

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getReg_id_short() {
		return reg_id_short;
	}

	public void setReg_id_short(String reg_id_short) {
		this.reg_id_short = reg_id_short;
	}

	public String getReg_name() {
		return reg_name;
	}

	public void setReg_name(String reg_name) {
		this.reg_name = reg_name;
	}

	public String getNx() {
		return nx;
	}

	public void setNx(String nx) {
		this.nx = nx;
	}

	public String getNy() {
		return ny;
	}

	public void setNy(String ny) {
		this.ny = ny;
	}

	public String getReg_id_long() {
		return reg_id_long;
	}

	public void setReg_id_long(String reg_id_long) {
		this.reg_id_long = reg_id_long;
	}

}
